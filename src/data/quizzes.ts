// Number of questions per quiz
const QUESTIONS_PER_QUIZ = 4;

// Fisher-Yates shuffle algorithm
function shuffleArray<T>(array: T[]): T[] {
    const shuffled = [...array];
    for (let i = shuffled.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    return shuffled;
}

// Get a random quiz by selecting random questions from the pool
export const getRandomQuiz = (): Quiz => {
    const shuffledQuestions = shuffleArray(questionPool);
    const selectedQuestions = shuffledQuestions.slice(0, QUESTIONS_PER_QUIZ);

    return {
        id: Date.now(), // Unique ID based on timestamp
        questions: selectedQuestions,
    };
};

